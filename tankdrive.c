#include "event.h"

#define ARM_SPEED_LOWER 100
#define ARM_SPEED_UPPER 50

task main()
{
	eventengine_t engine;
	event_t event;
	eventengine_init(&engine);

	engine.eventNone = true;
	engine.joysticks = false;
	engine.controller1 = true;
	engine.controller2 = false;

	float driveSpeed = 0.5;
	int controllerPov;

	struct {
		int arm1_target; // target rotation for arm1 motor
		int arm1_state; // current rotation for arm1 motor
		int arm1_lock; // true if we are moving the arm w/o servo's - just power
		int arm2_target;
		int arm2_state;
		int arm2_lock;
	} state_t;
	state_t state;

	memset(&state, 0, sizeof(state));

	while (true)
	{
		// Set wheel speeds
		motor[leftWheels] = (float) controllerValue(1, 1, 2) * driveSpeed;
		motor[rightWheels] = (float) controllerValue(1, 2, 2) * driveSpeed;

		// First, update states
		state.arm1_state = nMotorEncoder[motorARM1];
		state.arm2_state = nMotorEncoder[motorARM2];

		{
			// Lower joint controls
			if (joy1Btn(CONTROLLER_L1))
			{
				state.arm1_lock = true;
				motor[motorARM1] = ARM_SPEED_LOWER;
			}
			else if (joy1Btn(CONTROLLER_L2))
			{
				state.arm1_lock = true;
				motor[motorARM1] = -ARM_SPEED_LOWER;
			}
			else
			{
				motor[motorARM1] = 0;
				state.arm1_lock = false;
				state.arm1_state = nMotorEncoder[motorARM1]; // refresh state for accuracy
				state.arm1_target = state.arm1_state;
			}

			// Upper joint controlls
			if (joy1Btn(CONTROLLER_R1))
			{
				state.arm2_lock = true;
				motor[motorARM2] = ARM_SPEED_UPPER;
			}
			else if (joy1Btn(CONTROLLER_R2))
			{
				state.arm2_lock = true;
				motor[motorARM2] = -ARM_SPEED_UPPER;
			}
			else
			{
				motor[motorARM2] = 0;
				state.arm2_lock = false;
				state.arm2_state = nMotorEncoder[motorARM2]; // refresh state for accuracy
				state.arm2_target = state.arm2_state;
			}
		}

		// Do arm movements
		{
			// Now set the motors to go the correct direction
			if (!state.arm1_lock)
			{
				if (abs(state.arm1_target - state.arm1_state) > 50)
					motor[motorARM1] = state.arm1_target > state.arm1_state ? 100 : -100;
				else
					motor[motorARM1] = 0;
			}

			if (!state.arm2_lock)
			{
				if (abs(state.arm2_target - state.arm2_state) > 50)
					motor[motorARM2] = state.arm2_target > state.arm1_state ? 100 : -100;
				else
					motor[motorARM2] = 0;
			}
		}
	}
}
