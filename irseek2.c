#include "util.h"

task main()
{
	{
		// Step 1: drive until perpendicular
		forward();

		while (getIR(0) != 2
			&& getIR(1) != 8) {
			;
		}
		stopMovement();
		forward(-1.0);
		wait1Msec(100);
		stopMovement();
	}

	{
		// Step 2: rotate until on sum of 10 (IR)
		rotateLeft();
		while (getIRSum() != 10) {
				;
		}
		nxtDisplayTextLine(0, "IR:%d", getIRSum());
		stopMovement();
	}

	unpackArm();

	{
		// Step 3: drive and try to stay straight
        // TODO: break when close enough for arm to do its stuff
		while (true) {
			int sum = getIRSum();

			if (sum == 10) {
				forward();
			}
			else if (sum < 10) {
				rotateLeft(0.3);
			}
			else if (sum > 10) {
				rotateRight(0.3);
			}
		}
	}
}
